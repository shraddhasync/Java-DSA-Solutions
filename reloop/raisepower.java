package reloop;

public class raisepower {
    public static void main(String[] args) {
        int a=3;
        int b=5;

        int ans=1;

        for(int i=1;i<=b;i++){
            int e=a*1;
            ans=ans*e;
        }
        System.out.println(a+"^"+b+" = "+ans);
    }
    
}
