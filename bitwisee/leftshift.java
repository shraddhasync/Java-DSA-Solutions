public class leftshift {
    public static void main(String[] args) {
        System.out.println(5>>2);
    }
    
}
// binary left shift:
// a<<b    (a=no.which we shift)(b=how many bits we shift left)

// eg=5<<2
//  5=0000101
//  5<<2=0010100=20

//  formula for binary left shift= a<<b=a*2^b=5*2^2=20

  

